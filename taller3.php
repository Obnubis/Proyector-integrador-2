                <span class="text"><strong>Objetivos del Taller:</strong></span>
                <span class="text"><strong>Entender el TDAH y sus implicaciones académicas: </strong> Se proporcionará información sobre el TDAH y cómo afecta el aprendizaje y la concentración en el entorno educativo.
