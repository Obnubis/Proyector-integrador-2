<script>
    function validarFormulario() {
        var respuestas = document.querySelectorAll('input[type="radio"]:checked');
        var totalPreguntas = 20; // Total de preguntas en el formulario
        var respuestasContadas = {
            'a': 0,
            'b': 0,
            'c': 0
        };

        respuestas.forEach(function(respuesta) {
            var valorRespuesta = respuesta.id.substr(2, 1); // Obtener la letra de la opción seleccionada
            respuestasContadas[valorRespuesta]++;
        });

        if (respuestas.length < totalPreguntas) {
            document.getElementById('mensaje-error').style.display = 'block';
        } else {
            document.getElementById('mensaje-error').style.display = 'none';
            var porcentajeTDAH = ((respuestasContadas['a'] * 1 + respuestasContadas['b'] * 0.5) / totalPreguntas) * 100; // Calculamos el porcentaje de TDAH
            porcentajeTDAH = Math.random() * (100 - 50) + 50; // Generar un porcentaje aleatorio entre 50% y 100%
            alert('Tu porcentaje de TDAH es aproximadamente: ' + porcentajeTDAH.toFixed(2) + '%');
        }
    }
